#!/bin/bash
# ==========================================
# Docker 开发环境停止脚本 (Linux)
# ==========================================

echo "========================================"
echo "  停止 Docker 开发环境"
echo "========================================"
echo ""

# 进入 deploy 目录
cd "$(dirname "$0")/../deploy"

echo "正在停止所有服务..."
docker-compose down 2>/dev/null || docker compose down

if [ $? -ne 0 ]; then
    echo ""
    echo "[错误] 停止失败"
    exit 1
fi

echo ""
echo "========================================"
echo "  所有服务已停止"
echo "========================================"
echo ""
